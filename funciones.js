function cambiarcolor() {
    document.body.style.backgroundColor = 'black';
    document.getElementById('text').style.color = 'white';
    document.getElementById('cabecera').style.backgroundColor = '#0B0B61';
    document.getElementById('pie').style.backgroundColor = '#0B0B61';
    document.getElementById('derechos').style.color = 'white';
    document.getElementById('links').style.color = 'white';
}

function normal() {
    document.body.style.backgroundColor = 'white';
    document.getElementById('text').style.color = 'black';
    document.getElementById('cabecera').style.backgroundColor = 'cyan';
    document.getElementById('pie').style.backgroundColor = 'cyan';
    document.getElementById('derechos').style.color = 'black';
}